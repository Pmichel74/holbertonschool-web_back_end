#!/bin/usr/bin/python3
"""
    1-concat.py
    Module has function that returns the concatenation of two strings
"""


def concat(str1: str, str2: str) -> str:
    """
        Function that returns the concatenation of two strings
        Args:
            str1: first string
            str2: second string
        Returns:
            concatenation of str1 and str2
    """
    return str1 + str2
